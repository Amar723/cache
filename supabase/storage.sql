-- Cache — Storage bucket + policies for profile avatars.
-- Run after schema.sql and rls.sql.
--
-- Avatars are public-read (they appear on the profile and, in Phase 2, on
-- friends' screens) but writable only by their owner. Each user writes into a
-- folder named after their auth uid: `<uid>/avatar.<ext>`, which the policies
-- below enforce via storage.foldername().

insert into storage.buckets (id, name, public)
values ('avatars', 'avatars', true)
on conflict (id) do nothing;

create policy "Avatar images are publicly readable"
  on storage.objects for select
  using (bucket_id = 'avatars');

create policy "Users can upload their own avatar"
  on storage.objects for insert
  with check (
    bucket_id = 'avatars'
    and auth.uid()::text = (storage.foldername(name))[1]
  );

create policy "Users can update their own avatar"
  on storage.objects for update
  using (
    bucket_id = 'avatars'
    and auth.uid()::text = (storage.foldername(name))[1]
  );

create policy "Users can delete their own avatar"
  on storage.objects for delete
  using (
    bucket_id = 'avatars'
    and auth.uid()::text = (storage.foldername(name))[1]
  );
